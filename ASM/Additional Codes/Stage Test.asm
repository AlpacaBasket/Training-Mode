patchLoop:
#Get offset and replacement data
  lwz r3,0x0(r5)
  lwz r4,0x4(r5)
#Check if end of data
  extsb r0,r3
  cmpwi r0,-1
  beq endPatchLoop
#Replace data
  add r3,r3,r6
  stw r4,0x0(r3)
  addi r5,r5,0x8
  b patchLoop
endPatchLoop:
  b GoToExit

GoToExit:
b	Exit

